package com.netbull.shop.manage.controller;

/**
 * 定义抽象处理类,service注入、公共方法
 * @author lvan
 *
 */
public class AbstractIndexController {

	private static final long serialVersionUID = 1L;
	
	/**
	 * 加密/校验流程： 1. 将token、timestamp、nonce三个参数进行字典序排序 2.
	 * 将三个参数字符串拼接成一个字符串进行sha1加密 3. 开发者获得加密后的字符串可与signature对比，标识该请求来源于微信
	 */
	protected String signature;
	protected String timestamp;
	protected String nonce;
	protected String echostr;
	
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getNonce() {
		return nonce;
	}
	public void setNonce(String nonce) {
		this.nonce = nonce;
	}
	public String getEchostr() {
		return echostr;
	}
	public void setEchostr(String echostr) {
		this.echostr = echostr;
	}
}
